#ingrese un numero de forma manual e imprima si es mayor que 100 o menor que 100
#
condicion = 100
valor = int(input("inserte un numero: "))

if valor < condicion:
    print("el numero ingresado es menor que ", condicion)
elif valor > condicion:
    print("el numero ingresado es mayor que ", condicion)
else:
    print("ingresaste 100")


